package pointcards.game.pointsalad.criteria;

/**
 * The {@code CriteriaType} enumeration defines the different types of criteria
 * available in the Point Salad game. Each type represents a specific scoring
 * rule
 * that can be applied to a player's hand of cards.
 */
public enum CriteriaType {
    MOST,
    FEWEST,
    EVEN,
    ODD,
    EACH,
    PER,
    SET,
    MOST_TOTAL,
    AT_LEAST,
    FEWEST_TOTAL,
    PER_MISSING_TYPE;
}
